package com.javaprograms;

public class Multiplicationprogram {

	public static void main(String[] args) {
		
		int[] a = {2,5,8,9} ;
		
		for(int i=0;i<a.length;i++)
		{
			System.out.println(a[i]+" Table is Starting");
			
			{
				
			for(int j=1;j<=10;j++ )	
			
			System.out.println(a[i]+"*"+j+"="+a[i]*j);
			
			System.out.println("-------------");
			
		}
			
		}
		
		

	}

}
